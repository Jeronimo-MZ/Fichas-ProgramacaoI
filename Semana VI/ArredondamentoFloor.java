public class ArredondamentoFloor {
    public static void main(String[] args){
        System.out.println("5.2 arredondado para o inteiro anterior fica:"+ Math.floor(5.2));
        System.out.println("5.6 arredondado para o inteiro anterior fica:"+ Math.floor(5.6));
        System.out.println("-5.6 arredondado para o inteiro anterior fica:"+ Math.floor(-5.8));
    }
}
