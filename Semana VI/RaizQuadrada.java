public class RaizQuadrada {
    public static void main(String[] args) {
        
        float raiz1 = (float) Math.sqrt(900);
        float raiz2 = (float) Math.sqrt(30.25);
        System.out.println("A raiz quadrada de 900 é " + raiz1);
        System.out.println("A raiz quadrada de 30.25 é " + raiz2);
    }
}
